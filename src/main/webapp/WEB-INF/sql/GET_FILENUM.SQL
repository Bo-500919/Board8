CREATE SEQUENCE SEQ_FILE_NUM
START WITH 1  — 시퀀스의 시작 값
INCREMENT BY 1  — 증가 값
NOCACHE  — 캐시 사용 안 함
NOCYCLE;  — 다시 시작하지 않음

CREATE FUNCTION GET_FILENUM RETURN NUMBER
AS V_NUM NUMBER(10);
BEGIN
  SELECT (SELECT NVL(MAX(FILE_NUM),0)+1 FROM FILES)
    INTO V_NUM
    FROM DUAL;
RETURN V_NUM;
END;
